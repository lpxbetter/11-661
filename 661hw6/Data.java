public class Data {
	int index;
	String tags;
	public Data(int index, String tags) {
		super();
		this.index = index;
		this.tags = tags;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
}